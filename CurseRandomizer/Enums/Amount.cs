namespace CurseRandomizer;

public enum Amount
{
    /// <summary>
    /// 1 to 3% will be cursed items.
    /// </summary>
    Few,

    /// <summary>
    /// 4 to 6% will be cursed items.
    /// </summary>
    Some,

    /// <summary>
    /// 7 to 9% will be cursed items.
    /// </summary>
    Medium,

    /// <summary>
    /// 10 to 12% will be cursed items.
    /// </summary>
    Many,

    /// <summary>
    /// 13 to 15% will be cursed items.
    /// </summary>
    OhOh,

    /// <summary>
    /// The player can decide how many curses should be placed.
    /// </summary>
    Custom
}
