namespace CurseRandomizer;

public enum Amount
{
    /// <summary>
    /// Up to 5 will be placed.
    /// </summary>
    Few,

    /// <summary>
    /// Up to 10 will be placed.
    /// </summary>
    Some,

    /// <summary>
    /// Up to 15 will be placed.
    /// </summary>
    Medium,

    /// <summary>
    /// Up to 20 will be placed.
    /// </summary>
    Many,

    /// <summary>
    /// Up to 30 will be placed.
    /// </summary>
    OhOh,

    /// <summary>
    /// The player can decide how many curses should be placed.
    /// </summary>
    Custom
}
